public class Ex1 {
    public static void main(String[] args) {
        int age = 34;
        System.out.println(age);
        age = 43;
        System.out.println(age);
        
        double price = 5.7;
        System.out.println(price);
        price = 1;
        System.out.println(price);

        long foo = 1345678654356L;
        System.out.println(foo);

        price = age * foo + price;
        System.out.println(price);

        int hex = 0xabce;
        System.out.println(hex);
    }
}
